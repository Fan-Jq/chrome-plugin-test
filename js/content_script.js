function stand(t) {

    var nextline = "";
    nextline = t.split("，").join("");
    nextline = nextline.split("（").join("");
    nextline = nextline.split("）").join("");
    nextline = nextline.split("。").join("");
    nextline = nextline.split("“").join("");
    nextline = nextline.split("”").join("");
    nextline = nextline.split("　").join("");
    nextline = nextline.split(" ").join("");
    nextline = nextline.split("__").join("");
    nextline = nextline.split("_").join("");
    nextline = nextline.split(".").join("");
    nextline = nextline.split("？").join("");
    nextline = nextline.split("：").join("");
    nextline = nextline.split("(").join("");
    nextline = nextline.split(")").join("");
    nextline = nextline.split(":").join("");
    nextline = nextline.split("、").join("");
    nextline = nextline.split("；").join("");
    nextline = nextline.split(";").join("");
    nextline = nextline.split("《").join("");
    nextline = nextline.split("》").join("");
    nextline = nextline.split("．").join("");
    nextline = nextline.split("&nbsp").join("");
    nextline.replace("(\n|\r|(\r\n)|(\u0085)|(\u2028)|(\u2029))", "")
    return nextline;
}
